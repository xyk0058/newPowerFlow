package com.dhcc.model;

public class Bus {
	private double P;
	private double Q;
	private int index;
	private double V;
	public double getP() {
		return P;
	}
	public void setP(double p) {
		P = p;
	}
	public double getQ() {
		return Q;
	}
	public void setQ(double q) {
		Q = q;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int i) {
		this.index = i;
	}
	public double getV() {
		return V;
	}
	public void setV(double v) {
		V = v;
	}
	
}
